def get_opening_name(eco: str, fallback: str = None) -> str:
    """Get opening name from ECO code."""
    if eco in ECO_OPENINGS:
        return ECO_OPENINGS[eco]
    if fallback and fallback != "Unknown Opening" and fallback != eco:
        return fallback
    return eco if eco else "Unknown Opening"


def get_opening_family(eco: str) -> str:
    """Get the broad opening family from ECO code."""
    if not eco:
        return "Unknown"

    letter = eco[0].upper()
    num = int(eco[1:]) if eco[1:].isdigit() else 0

    if letter == 'A':
        if num < 10:
            return "Uncommon Openings"
        elif num < 40:
            return "English Opening"
        elif num < 50:
            return "Queen's Pawn Game"
        elif num < 80:
            return "Indian Defenses"
        else:
            return "Dutch Defense"
    elif letter == 'B':
        if num < 10:
            return "Uncommon King's Pawn"
        elif num < 20:
            return "Caro-Kann Defense"
        else:
            return "Sicilian Defense"
    elif letter == 'C':
        if num < 20:
            return "French Defense"
        elif num < 30:
            return "King's Pawn Game"
        elif num < 40:
            return "King's Gambit"
        elif num < 50:
            return "Open Games"
        elif num < 60:
            return "Italian Game"
        else:
            return "Ruy Lopez"
    elif letter == 'D':
        if num < 6:
            return "Queen's Pawn Game"
        elif num < 10:
            return "Queen's Gambit"
        elif num < 20:
            return "Slav Defense"
        elif num < 30:
            return "Queen's Gambit Accepted"
        elif num < 70:
            return "Queen's Gambit Declined"
        else:
            return "Grunfeld Defense"
    elif letter == 'E':
        if num < 10:
            return "Catalan Opening"
        elif num < 20:
            return "Queen's Indian"
        elif num < 60:
            return "Nimzo-Indian Defense"
        else:
            return "King's Indian Defense"

    return "Unknown"
